# Ejercicio 5
# Convertir un valor dado en grados Fahrenheit a grados Celsius usando la fórmula:

# Pedimos al usuario que ingrese la temperatura en grados Fahrenheit
grados_fahrenheit = float(input("Ingresa la temperatura en grados Fahrenheit: "))

# Aplicamos la fórmula para convertir a grados Celsius
grados_celsius = (grados_fahrenheit - 32) * 5 / 9

# Mostramos el resultado
print("La temperatura en grados Celsius es:", grados_celsius)
