# Ejercicio 11
# Mostrar la distancia (valor absoluto de la diferencia) entre dos números ingresados.

# Pedir los dos números al usuario
numero_1 = float(input("Ingresa el primer número: "))
numero_2 = float(input("Ingresa el segundo número: "))

# Calcular la distancia (valor absoluto de la diferencia)
distancia = abs(numero_1 - numero_2)

# Mostrar el resultado
print(f"Distancia entre los dos números: {distancia}")